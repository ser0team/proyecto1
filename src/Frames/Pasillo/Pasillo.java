        idpasillo = Integer.parseInt(jtpasillo.getValueAt(jtpasillo.getSelectedRow(), 0).toString());
        System.out.println(idpasillo);
        jtxtpasillo.setText(jtpasillo.getValueAt(jtpasillo.getSelectedRow(), 1).toString());
        jtxtdescripcion.setText(jtpasillo.getValueAt(jtpasillo.getSelectedRow(), 2).toString());
