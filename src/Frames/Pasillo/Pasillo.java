    private void jtpasilloKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtpasilloKeyReleased

        jtpasillo.setSurrendersFocusOnKeystroke(true);
        if(evt.getKeyChar() == KeyEvent.VK_ENTER){
            pas.jbmodificar(jtpasillo.getValueAt(jtpasillo.getSelectedRow(), 1).toString(), 
                    jtpasillo.getValueAt(jtpasillo.getSelectedRow(), 2).toString(), 
                    Integer.parseInt(jtpasillo.getValueAt(jtpasillo.getSelectedRow(), 3).toString()), 
                    Menu.idusu, 
                    Menu.idcompania, 
                    Integer.parseInt(jtpasillo.getValueAt(jtpasillo.getSelectedRow(), 0).toString()));
            pas.jbcancelar(jtxtpasillo, jtxtdescripcion);
            pas.mostrarRegistros(jtpasillo, 0, 0, Menu.idcompania);
        }
        
    }//GEN-LAST:event_jtpasilloKeyReleased

