    private void jtalmacenesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtalmacenesKeyReleased

        jtalmacenes.setSurrendersFocusOnKeystroke(true);
        if(evt.getKeyChar() == KeyEvent.VK_ENTER){
            alm.jbmodificar(jtalmacenes.getValueAt(jtalmacenes.getSelectedRow(), 1).toString(), 
                    jtalmacenes.getValueAt(jtalmacenes.getSelectedRow(), 2).toString(), 
                    Menu.idusu, 
                    Menu.idcompania, 
                    Integer.parseInt(jtalmacenes.getValueAt(jtalmacenes.getSelectedRow(), 0).toString()));
            alm.jbcancelar(jtxtalmacen, jtxtdescripcion);
            alm.mostrarRegistros(jtalmacenes, 0, Menu.idcompania);
        }
        
    }//GEN-LAST:event_jtalmacenesKeyReleased

