    private void jtajustesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtajustesMouseClicked

        try {
            EdicionPrecios.jtxtrazonajuste.setText(jtajustes.getValueAt(jtajustes.getSelectedRow(), 1).toString());
            EdicionPrecios.jtxtcantidadajuste.setText(jtajustes.getValueAt(jtajustes.getSelectedRow(), 2).toString());
            String tipoajuste = jtajustes.getValueAt(jtajustes.getSelectedRow(), 3).toString();
            if(tipoajuste.equals("")){
                EdicionPrecios.jckajuste.setSelected(false);
            }else{
                EdicionPrecios.jckajuste.setSelected(true);
            }
            String fecha = jtajustes.getValueAt(jtajustes.getSelectedRow(), 4).toString();
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            Date date = df.parse(fecha);
            EdicionPrecios.jdcfecha.setDate(date);
            EdicionPrecios.jtxtidcompraproducto.setText(jtajustes.getValueAt(jtajustes.getSelectedRow(), 5).toString());
            EdicionPrecios.jtxtidproducto.setText(jtajustes.getValueAt(jtajustes.getSelectedRow(), 6).toString());
            EdicionPrecios.jtxtproducto.setText(jtajustes.getValueAt(jtajustes.getSelectedRow(), 7).toString());

            Instancias._ajustes = null;
            this.dispose();
        } catch (ParseException ex) {
            Logger.getLogger(ConsultaAjustes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jtajustesMouseClicked

