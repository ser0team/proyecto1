    private void jbguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbguardarActionPerformed

        if(jtxtidalmacen.getText().equals("") ||
                jtxtidpasillo.getText().equals("") ||
                jtxtidanaquel.getText().equals("") ||
                jtxtidbin.getText().equals("") ||
                jtxtidproducto.getText().equals("")){
            jtm.jTextAreaMessage("Existen campos vacios");
        }else{
            mov.jbguardar(Integer.parseInt(jtxtidalmacen.getText()), 
                    Integer.parseInt(jtxtidpasillo.getText()), 
                    Integer.parseInt(jtxtidanaquel.getText()), 
                    Integer.parseInt(jtxtidbin.getText()), 
                    Integer.parseInt(jtxtidproducto.getText()), 
                    Menu.idusu,
                    Menu.idcompania);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jbguardarActionPerformed

