    private void jtbinsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbinsMouseClicked

        idbin = Integer.parseInt(jtbins.getValueAt(jtbins.getSelectedRow(), 0).toString());
        jtxtbin.setText(jtbins.getValueAt(jtbins.getSelectedRow(), 1).toString());
        jtxtdescripcion.setText(jtbins.getValueAt(jtbins.getSelectedRow(), 2).toString());
        jtxtidanaquel.setText(jtbins.getValueAt(jtbins.getSelectedRow(), 3).toString());
        
    }//GEN-LAST:event_jtbinsMouseClicked

    private void jtbinsKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtbinsKeyReleased

        jtbins.setSurrendersFocusOnKeystroke(true);
        
        if(evt.getKeyChar() == KeyEvent.VK_ENTER){        
            bin.jbmodificar(jtbins.getValueAt(jtbins.getSelectedColumnCount(), 1).toString(), 
                    jtbins.getValueAt(jtbins.getSelectedRow(), 2).toString(), 
                    Integer.parseInt(jtbins.getValueAt(jtbins.getSelectedRow(), 3).toString()), 
                    Menu.idusu, 
                    Menu.idcompania, 
                    Integer.parseInt(jtbins.getValueAt(jtbins.getSelectedRow(), 0).toString()));
            bin.jbcancelar(jtxtbin, jtxtdescripcion, jtxtidanaquel);
            bin.mostrarRegistros(jtbins, Integer.parseInt(jtxtidanaquel.getText()), 0, Menu.idcompania);
        }
        
    }//GEN-LAST:event_jtbinsKeyReleased

