    private void jmi_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmi_eliminarActionPerformed

         if(String.valueOf(idbin).equals("")){
            jtm.jTextAreaMessage("No se ha seleccionado un bin");
        }else{
            bin.jbeliminar(idbin, Menu.idcompania);
            bin.jbcancelar(jtxtbin, jtxtdescripcion, jtxtidanaquel);
            bin.mostrarRegistros(jtbins, Integer.parseInt(jtxtidanaquel.getText()), 0, Menu.idcompania);
        }
        
    }//GEN-LAST:event_jmi_eliminarActionPerformed

