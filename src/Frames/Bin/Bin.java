    private void jbguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbguardarActionPerformed

        if(jtxtbin.getText().equals("") || jtxtidanaquel.getText().equals("")){
            jtm.jTextAreaMessage("Existen campos vacios");
        }else{
            bin.jbguardar(jtxtbin.getText(), 
                    jtxtdescripcion.getText(), 
                    Integer.parseInt(jtxtidanaquel.getText()),
                    Menu.idusu,
                    Menu.idcompania);
            bin.jbcancelar(jtxtbin, jtxtdescripcion, jtxtidanaquel);
            bin.mostrarRegistros(jtbins, Integer.parseInt(jtxtidanaquel.getText()), 0, Menu.idcompania);
        }
        
    }//GEN-LAST:event_jbguardarActionPerformed

