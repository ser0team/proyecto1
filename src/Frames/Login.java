    private void jlusuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlusuarioMouseClicked

        path = "/Imagenes/usuario.png";
        url = this.getClass().getResource(path);
        fenix = new ImageIcon(url); 
        
        jlusuario.setVisible(false);
        jtusuario.setEnabled(true);
        jtcont.setEnabled(true);
        jbaceptar.setEnabled(true);
        rol = "adm";
        Menu.rol_login = rol;
        JLabel jladministrador = new JLabel();
        jladministrador.setName("jladministrador");
        jladministrador.setText("Admin.:");
        jladministrador.setPreferredSize(new Dimension(118, 30));
        jladministrador.setBounds(15, 12, 118, 30);
        jladministrador.setIcon(fenix);
        jladministrador.setVisible(true);
        jladministrador.addMouseListener(new MouseListener(){
            @Override
            public void mouseClicked(MouseEvent e) {
                jladministrador.setVisible(false);
                jlusuario.setVisible(true);
                rol = "usu";                
                jtusuario.setEnabled(false);
                jtcont.setEnabled(false);
                jbaceptar.setEnabled(false);
            }

            @Override
            public void mousePressed(MouseEvent e) {
               
            }

            @Override
            public void mouseReleased(MouseEvent e) {
                
            }

            @Override
            public void mouseEntered(MouseEvent e) {
                border = new SoftBevelBorder(BevelBorder.RAISED);
                margin = jladministrador.getBorder();
                jladministrador.setBorder(new CompoundBorder(border, margin));
            }

            @Override
            public void mouseExited(MouseEvent e) {
                jladministrador.setBorder(null);
            }
            
        });
        jplogin.add(jladministrador);
        
    }//GEN-LAST:event_jlusuarioMouseClicked

