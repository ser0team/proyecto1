    private void jbeliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbeliminarActionPerformed

        if(String.valueOf(idmateria).equals("")){
            jtm.jTextAreaMessage("No se ha seleccionado un identificador");
        }else{
            mat.eliminar(idmateria);
            mat.cancelar(jtxtnombre, jtxtdescripcion, jtxtfecha, jtxtalmacen, jtxtpasillo, jtxtanaquel, jtxtbin);
            mat.mostrar(jtmateria, "");
        }

    }//GEN-LAST:event_jbeliminarActionPerformed

