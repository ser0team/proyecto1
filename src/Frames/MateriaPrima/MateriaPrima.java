    private void jmiguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiguardarActionPerformed

        if(jtxtnombre.getText().equals("") || jtxtalmacen.getText().equals("") 
                || jtxtpasillo.getText().equals("") || jtxtanaquel.getText().equals("")
                || jtxtbin.getText().equals("")){
            jtm.jTextAreaMessage("Existen campos vacios");
            
        }else{
            mat.guardar(jtxtnombre.getText(), jtxtdescripcion.getText(), jtxtfecha, 
                    Integer.parseInt(jtxtalmacen.getText()), 
                    Integer.parseInt(jtxtpasillo.getText()), 
                    Integer.parseInt(jtxtanaquel.getText()), 
                    Integer.parseInt(jtxtbin.getText()), Menu.idusu, Menu.idcompania);
            mat.cancelar(jtxtnombre, jtxtdescripcion, jtxtfecha, jtxtalmacen, jtxtpasillo, jtxtanaquel, jtxtbin);
            mat.mostrar(jtmateria, "");
        }

    }//GEN-LAST:event_jmiguardarActionPerformed

