    private void jbcorteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbcorteActionPerformed

         if(jdcfecha.getDate()==null){
            jtm.jTextAreaMessage("No se ha seleccionado fecha");
        }else{
             jlcaja.setText("");
             jltotal.setText("");
             jlcompras.setText("");
             jlcorte.setText("");
             corte.total = 0.00;
             corte.compras = 0.00;
             corte.caja = 0.00;
             corte.cortefinal = 0.00;
             corte.corte = 0.00;
             cor.obtenerCaja(jdcfecha, jlcaja, Menu.idcompania);
             cor.obtenCompras(jdcfecha, jlcompras, Menu.idcompania);
             cor.obtenerTotal(jdcfecha, jltotal, jlcorte, jlcompras, Menu.idcompania);
        }            
        
    }//GEN-LAST:event_jbcorteActionPerformed

