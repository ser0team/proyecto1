    private void jtalmacenesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtalmacenesMouseClicked
        
        switch (whoisselected) {
            case "dev_jtxtidalmacen":
                Devoluciones.jtxtidalmacen.setText(jtalmacenes.getValueAt(jtalmacenes.getSelectedRow(), 0).toString());
                Instancias._listaralmacenes = null;
                this.dispose();
                break;
            case "ent_jbidalmacen":
                EntradasProducto.jtxtidalmacen.setText(jtalmacenes.getValueAt(jtalmacenes.getSelectedRow(), 0).toString());
                Instancias._listaralmacenes = null;
                this.dispose();
                break;
            case "movimientosalmacen":
                MovimientosAlmacen.jtxtidalmacen.setText(jtalmacenes.getValueAt(jtalmacenes.getSelectedRow(), 0).toString());
                Instancias._listaralmacenes = null;
                this.dispose();
                break;
            case "pas_jbidalmacen":
                Pasillo.jtxtidalmacen.setText(jtalmacenes.getValueAt(jtalmacenes.getSelectedRow(), 0).toString());
                Instancias._listaralmacenes = null;
                this.dispose();
                break;
            case "origen_almacen":
                MovimientosAlmacen.jtxtorialmacen.setText(jtalmacenes.getValueAt(jtalmacenes.getSelectedRow(), 0).toString());
                Instancias._listaralmacenes = null;
                this.dispose();
                break;
            default:
                break;
        }
        
    }//GEN-LAST:event_jtalmacenesMouseClicked

