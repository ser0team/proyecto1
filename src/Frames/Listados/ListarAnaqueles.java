    public ListarAnaqueles() {
        initComponents();
        this.setLocationRelativeTo(null);
        jtanaqueles.setDefaultEditor(Object.class, null);
        th = jtanaqueles.getTableHeader();
        th.setBackground(Color.decode("#66535A"));
        th.setForeground(Color.WHITE);        
        switch (whoisselected) {
            case "dev_jtxtidanaquel":
                ana.mostrarRegistros(jtanaqueles, 
                        Integer.parseInt(Devoluciones.jtxtidpasillo.getText()), 
                        0, Menu.idcompania);               
                break;
            case "ent_idanaquel":
                ana.mostrarRegistros(jtanaqueles, 
                        Integer.parseInt(EntradasProducto.jtxtidpasillo.getText()),
                        0, Menu.idcompania);
                break;
            case "movimientosanaquel":
                ana.mostrarRegistros(jtanaqueles, 
                        Integer.parseInt(MovimientosAlmacen.jtxtidpasillo.getText()),
                        0, Menu.idcompania);
                break;
            case "bin_jbidanquel":
                ana.mostrarRegistros(jtanaqueles, 0, 0, Menu.idcompania);
                break;
            case "origen_anaquel":
                ana.mostrarRegistros(jtanaqueles, 
                        Integer.parseInt(MovimientosAlmacen.jtxtoripasillo.getText()),
                        Integer.parseInt(MovimientosAlmacen.jtxtidproducto.getText()),
                        Menu.idcompania);
                break;
            default:  
                ana.mostrarRegistros(jtanaqueles, 0, 0, Menu.idcompania);
                break;
        }
    }

