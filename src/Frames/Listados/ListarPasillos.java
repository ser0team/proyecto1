    public ListarPasillos() {
        initComponents();
        this.setLocationRelativeTo(null);
        th = jtpasillos.getTableHeader();
        th.setBackground(Color.decode("#66535A"));
        th.setForeground(Color.WHITE);
        jtpasillos.setDefaultEditor(Object.class, null);
        switch (whoisselected) {
            case "dev_jtxtidpasillo":
                pas.mostrarRegistros(jtpasillos, Integer.parseInt(Devoluciones.jtxtidalmacen.getText()), 
                        0, Menu.idcompania);
                break;
            case "ent_jbidpasillo":
                pas.mostrarRegistros(jtpasillos, Integer.parseInt(EntradasProducto.jtxtidalmacen.getText()),
                        0, Menu.idcompania);
                break;
            case "movimientospasillo":
                pas.mostrarRegistros(jtpasillos, Integer.parseInt(MovimientosAlmacen.jtxtidalmacen.getText()), 
                        0, Menu.idcompania);
                break;
             case "ana_jbidpasillo":
                pas.mostrarRegistros(jtpasillos, 0, 0, Menu.idcompania);
                break;
             case "origen_pasillo":                 
                pas.mostrarRegistros(jtpasillos, 
                    Integer.parseInt(MovimientosAlmacen.jtxtorialmacen.getText()), 
                    Integer.parseInt(MovimientosAlmacen.jtxtidproducto.getText()), 
                    Menu.idcompania);           
                break;
            default:                
                pas.mostrarRegistros(jtpasillos, 0, 0, Menu.idcompania);
                break;

        }
        
    }

