    private void jtpasillosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtpasillosMouseClicked

        switch (whoisselected) {
            case "dev_jtxtidpasillo":
                Devoluciones.jtxtidpasillo.setText(jtpasillos.getValueAt(jtpasillos.getSelectedRow(), 0).toString());
                Instancias._listarpasillos = null;
                this.dispose();
                break;
            case "ent_jbidpasillo":
                EntradasProducto.jtxtidpasillo.setText(jtpasillos.getValueAt(jtpasillos.getSelectedRow(), 0).toString());
                Instancias._listarpasillos = null;
                this.dispose();
                break;
            case "movimientospasillo":
                MovimientosAlmacen.jtxtidpasillo.setText(jtpasillos.getValueAt(jtpasillos.getSelectedRow(), 0).toString());
                Instancias._listarpasillos = null;
                this.dispose();
                break;  
            case "ana_jbidpasillo":
                Anaqueles.jtxtidpasillo.setText(jtpasillos.getValueAt(jtpasillos.getSelectedRow(), 0).toString());
                Instancias._listarpasillos = null;
                this.dispose();
                break;
            case "origen_pasillo":
                MovimientosAlmacen.jtxtoripasillo.setText(jtpasillos.getValueAt(jtpasillos.getSelectedRow(), 0).toString());
                Instancias._listarpasillos = null;
                this.dispose();
                break;
            default:
                break;
        }
        
    }//GEN-LAST:event_jtpasillosMouseClicked

