    private void jtproveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtproveedoresMouseClicked

        if(whosSelected == Productos.jbproveedor){
            Productos.id = Integer.parseInt(jtproveedores.getValueAt(jtproveedores.getSelectedRow(), 0).toString());
            proveedor = jtproveedores.getValueAt(jtproveedores.getSelectedRow(), 1).toString();                     
            
            Productos.jtxtidproveedor.setText(String.valueOf(Productos.id));
            Productos.jtxtproveedor.setText(proveedor);
            this.dispose();
            Instancias._listaproveedores = null;
        }else if(whosSelected == Compras.jbcproveedor){
            Compras.idproveedor = Integer.parseInt(jtproveedores.getValueAt(jtproveedores.getSelectedRow(), 0).toString());
            proveedor = jtproveedores.getValueAt(jtproveedores.getSelectedRow(), 1).toString();    
            
            Compras.jtxtproveedor.setText(proveedor);
            this.dispose();
        }else if(whosSelected == ConsultaCompras.jbproveedor){
            ConsultaCompras.idproveedor = Integer.parseInt(jtproveedores.getValueAt(jtproveedores.getSelectedRow(), 0).toString());
            proveedor = jtproveedores.getValueAt(jtproveedores.getSelectedRow(), 1).toString();
            
            ConsultaCompras.jtxtproveedor.setText(proveedor);
            this.dispose();
        }       
        
    }//GEN-LAST:event_jtproveedoresMouseClicked

